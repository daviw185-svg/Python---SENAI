numero = 17
numero2 = 5

print(numero + numero2) # Adição
print(numero - numero2) # Subtração
print(numero * numero2) # Multiplicação
print(numero / numero2) # Divisão (sempre float)
print(numero // numero2) # Divisão inteira (descarta decimal)
print(numero % numero2) # Módulo (resto da divisão)
print(2 ** 8) # Potenciação

#Precedência - igual a matemática escola

