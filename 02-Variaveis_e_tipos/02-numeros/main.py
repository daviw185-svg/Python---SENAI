# INT -números inteiros
idade = 18
ano = 2026
negativo = -16
print (type (idade))
print (type (ano))
print (type (negativo))

# Float - número decimais (use o "." e não a ",")
altura = 1.67
preco = 3.57
nota = 9.5
print (type (altura))
print (type (preco))
print (type (nota))

# Armadilha Clássica do Float
print (0.1 + 0.2) # 0.30000000000000004